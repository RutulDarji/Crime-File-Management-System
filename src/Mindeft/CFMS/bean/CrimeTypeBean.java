package Mindeft.CFMS.bean;

public class CrimeTypeBean {

	int CrimeTypeId;
	String CrimeType=new String();
	String CrimeDetails=new String();

	public int getCrimeTypeId() {
		return CrimeTypeId;
	}
	public void setCrimeTypeId(int crimeTypeId) {
		CrimeTypeId = crimeTypeId;
	}
	public String getCrimeType() {
		return CrimeType;
	}
	public void setCrimeType(String crimeType) {
		CrimeType = crimeType;
	}
	public String getCrimeDetails() {
		return CrimeDetails;
	}
	public void setCrimeDetails(String crimeDetails) {
		CrimeDetails = crimeDetails;
	}
	
	


}


